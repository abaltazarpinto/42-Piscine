unsigned char	reverse_bits(unsigned char octet)
{
	return (((octet >> 7) & 1) <<7) | \
	       	(((octet >> 6) & 1) <<6) | \
       		(((octet >> 5) & 1) <<5) | \
		(((octet >> 4) & 1) <<4) | \
	 	(((octet >> 3) & 1) <<3) | \
		(((octet >> 2) & 1) <<2) | \
		(((octet >> 1) & 1) <<1) | \
		(((octet >> 0) & 1) <<0);	
}
	
