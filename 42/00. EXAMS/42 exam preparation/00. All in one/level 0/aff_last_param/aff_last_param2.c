#include <unistd.h>

int main(int ac, char **av)
{
	if(ac >= 2)
	{
		int x = ac - 1;
		int i = 0;

		while(av[x][i] != '\0')
		{
			write(1, &av[x][i], 1);
			i++;
		}
	}
	write(1, "\0", 1);
	return (0);
}
